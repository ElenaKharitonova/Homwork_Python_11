# –ó–∞–¥–∞—á–∞ 1. –ü–æ—Å—Ç—Ä–æ–π—Ç–µ –≥—Ä–∞—Ñ–∏–∫ —Ñ—É–Ω–∫—Ü–∏–∏ ùëì(ùë•)=5ùë•^2+10ùë•‚àí30
# –ü–æ –≥—Ä–∞—Ñ–∏–∫—É –æ–ø—Ä–µ–¥–µ–ª–∏—Ç–µ, –ø—Ä–∏ –∫–∞–∫–∏—Ö –∑–Ω–∞—á–µ–Ω–∏—è x –∑–Ω–∞—á–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏ –æ—Ç—Ä–∏—Ü–∞—Ç–µ–ª—å–Ω–æ.

import matplotlib.pyplot as plt

x_list = []
y_list = []

for x in range(-10,11):
    y = 5*x**2+10*x-30
    x_list.append(x)
    y_list.append(y)
plt.axhline(y=0,color='r',linestyle='dashdot')
plt.plot(y_list,color='k',linestyle='solid',label='f(x)=5x^2+10x-30')# –¥–∞–ª–∏ –Ω–∞–∑–≤–∞–Ω–∏–µ –ª–∏–Ω–∏–∏ –≥—Ä–∞—Ñ–∏–∫–∞ –¥–ª—è –≤—ã–≤–æ–¥–∞ –≤ –ª–µ–≥–µ–Ω–¥—É
plt.title('–ì—Ä–∞—Ñ–∏–∫ —Ñ—É–Ω–∫—Ü–∏–∏ f(x)', color='g')# –¥–∞–ª–∏ –Ω–∞–∑–≤–∞–Ω–∏–µ –≥—Ä–∞—Ñ–∏–∫—É
plt.xlim([0., 20.])# —Å–∫–æ—Ä—Ä–µ–∫—Ç–∏—Ä–æ–≤–∞–ª–∏ –ø–æ–≥—Ä–µ—à–Ω–æ—Å—Ç—å –æ—Å–∏ y, –ø—Ä–∏—Ç—è–Ω—É–ª–∏ –≤ –Ω–æ–ª—å
plt.xlabel('–û—Å—å x', fontsize=12,color='r')# –Ω–∞–∑–≤–∞–ª–∏ –≥–æ—Ä–∏–∑–æ–Ω—Ç–∞–ª—å–Ω—É—é –æ—Å—å
plt.ylabel('–û—Å—å y', fontsize=12,color='r')# –Ω–∞–∑–≤–∞–ª–∏ –≤–µ—Ä—Ç–∏–∫–∞–ª—å–Ω—É—é –æ—Å—å
plt.grid(which='major')# –≤–∫–ª—é—á–∏–ª–∏ –æ—Å–Ω–æ–≤–Ω—É—é —Å–µ—Ç–∫—É

plt.minorticks_on()# –≤–∫–ª—é—á–∏–ª–∏ –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–µ (–±–æ–ª–µ–µ –¥–µ—Ç–∞–ª—å–Ω—ã–µ) –æ—Ç–º–µ—Ç–∫–∏ –Ω–∞ –æ—Å—è—Ö

plt.grid(which='minor', linestyle=':')# –≤–∫–ª—é—á–∏–ª–∏ –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—É—é —Å–µ—Ç–∫—É

plt.legend(fontsize=12)# –≤—ã–≤–µ–ª–∏ –ª–µ–≥–µ–Ω–¥—É
plt.tight_layout()

plt.show()